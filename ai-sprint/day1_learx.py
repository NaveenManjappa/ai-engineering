# Single line comment starts with a # symbol
"""
Multiline comments can be enclosed in triple quotes " " or ' and can span multiple lines.
"""

print(1 + 1)
print(35 / 7)
print(10 / 3)

print(-7 % 3)

# Exponentiation x**y, x to the yth power
print(2**3)

print(5 // 3)

# Boolean operators
print(True and False)
print(True or False)

# True and False values are actually 1 and 0 but with different keywords
print(True + True)
print(True * 9)
print(False - 5)

# Compariosion operators look at the numerical value of True and False

print(0 == False)
print(2 > True)
print(-5 != False)
print(-5 != False)

# None,0,empty strings/lists/dicts/tuples/sets all evaluate to False
print(0 == False)
print(2 > True)
print(2 == True)
print(bool(0))
print(bool(""))
print(bool([]))
print(bool({}))
print(bool(set()))
print(bool(()))
print(bool(4))
print(bool(-6))
print(1 and 0)
print(1 and 0 and 2)

print("Equality is ==")
print(1 == 1)
print(1 == 2)
print("hello" == "hello")
print("hello" == "world")
print([1, 2, 3] == [1, 2, 3])
print([1, 2, 3] == [3, 2, 1])
